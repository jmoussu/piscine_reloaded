#include <unistd.h>

int		ft_recursive_factorial(int nb)
{
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	if (nb >= 13)
		return (0);
	nb = nb * (ft_recursive_factorial(nb - 1));
	return (nb);
}
