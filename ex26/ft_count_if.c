#include <stdio.h>

int		ft_count_if(char **tab, int (*f)(char*))
{
	int c;
	int i;

	i = 0;
	c = 0;
	while (tab[c] != 0)
	{
		if (f(tab[c]) == 1)
			i++;
		c++;
	}
	return (i);
}
